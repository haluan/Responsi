package org.com.responsi;

public abstract class Proyek {
	private String namaProyek;
	private double biayaProyek;
	private int lamaProyek;
	public String getNamaProyek() {
		return namaProyek;
	}
	public void setNamaProyek(String namaProyek) {
		this.namaProyek = namaProyek;
	}
	public double getBiayaProyek() {
		return biayaProyek;
	}
	public void setBiayaProyek(double biayaProyek) {
		this.biayaProyek = biayaProyek;
	}
	public int getLamaProyek() {
		return lamaProyek;
	}
	public void setLamaProyek(int lamaProyek) {
		this.lamaProyek = lamaProyek;
	}
	
	

}
